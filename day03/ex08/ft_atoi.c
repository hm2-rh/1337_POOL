int	ft_atoi(char *str)
{
	int i;
	int j;
	int neg;
	int res;

	i = 0;
	neg = 1;
	while (str[i] <= 32)
		i++;
	if (str[i] == '-')
		neg = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	j = i;
	while (str[i] >= 48 && str[i] <= 57)
		i++;
	i--;
	res = 0;
	while (j <= i)
	{
		res = res * 10 + (str[j] - 48);
		j++;
	}
	return (res * neg);
}
